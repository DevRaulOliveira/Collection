/*
OBJETIVO MOSTRAR AS COLUNAS FIRST NAME E LAST NAME DA TABELA CUSTOMER
COMBINANDO COM A COLUNA PAYMENT MOSTRANDO A COLUNA RENTAL ID E AMOUNT
