USE Sakila;

-- MODELO DE REGEXP SIMPLES
SELECT * FROM actor
WHERE first_name REGEXP	"a"; -- O OPERADOR REGEXP NESTA LINHA, RETORNA QUALQUER RESULTADO DA COLUNA "first_name" QUE TENHA A LETRA "a"
							-- NÃO PRECISA DO % PARA FAZER A BUSCA
                            -- CASO QUEIRA DIRECIONAR, UTILIZAR "^" ANTES/DEPOIS DO FILTRO QUE DESEJO APLICAR NESTE CASO REPRESENTA, INCIAM/TERMINAM COM A EXPRESSÃO OU LETRA A SEGUIR

-- MODELO DE REGEXP COM MAIS DE UM TERMO OU LETRA
SELECT * FROM country
WHERE country REGEXP "a|b" ;  -- USANDO REGEXP PARA BUSCAR APENAS VALORES QUE COMECEM COM 2 

-- MODELLO DE REGEXP COMPARANDO RELACIONANDO VALORES          

SELECT * FROM film
WHERE title REGEXP "^a[cim]" -- BUSCOU TERMOS QUE COMECEM COM "a" E TENHAM OU "c" OU "i" ou "m" NA SEQUENCIA
